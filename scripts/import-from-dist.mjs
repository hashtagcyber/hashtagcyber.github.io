#!/usr/bin/env node
// One-time import: read existing dist/{collection}/posts/*/index.html and
// emit src/content/{collection}/{slug}.md with frontmatter + turndown body.

import { readFileSync, writeFileSync, readdirSync, statSync, mkdirSync } from 'node:fs';
import { join, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';
import { load } from 'cheerio';
import TurndownService from 'turndown';

const ROOT = dirname(fileURLToPath(import.meta.url)) + '/..';
const COLLECTIONS = ['leadership', 'security', 'talks'];

const turndown = new TurndownService({
  headingStyle: 'atx',
  codeBlockStyle: 'fenced',
  bulletListMarker: '-',
  emDelimiter: '_',
});

// keep <hr> as `---` separator (default already does)
// strip out astro-cid attributes and id attributes (markdown will regenerate ids)
turndown.remove((node) => node.nodeName === 'SCRIPT' || node.nodeName === 'STYLE');

// Custom rule: any unknown element with attributes → output its inner content,
// preserving the children. This handles things like <insert classic comic strip>.
turndown.addRule('passthroughUnknown', {
  filter: (node) => {
    const known = new Set([
      'A','ABBR','ARTICLE','ASIDE','B','BLOCKQUOTE','BR','CODE','DEL','DETAILS',
      'DIV','DL','DT','DD','EM','FIGURE','FIGCAPTION','FOOTER','H1','H2','H3','H4',
      'H5','H6','HEADER','HR','I','IMG','INS','KBD','LI','MAIN','NAV','OL','P','PRE',
      'Q','S','SECTION','SMALL','SPAN','STRONG','SUB','SUMMARY','SUP','TABLE','TBODY',
      'TD','TFOOT','TH','THEAD','TR','U','UL'
    ]);
    return node.nodeType === 1 && !known.has(node.nodeName);
  },
  replacement: (content) => content,
});

function listDirs(p) {
  try {
    return readdirSync(p)
      .map((n) => join(p, n))
      .filter((f) => statSync(f).isDirectory());
  } catch {
    return [];
  }
}

function ensureDir(p) {
  mkdirSync(p, { recursive: true });
}

function yamlEscape(s) {
  return String(s).replace(/"/g, '\\"');
}

function frontmatter(data) {
  const lines = ['---'];
  if (data.title) lines.push(`title: "${yamlEscape(data.title)}"`);
  if (data.description) lines.push(`description: "${yamlEscape(data.description)}"`);
  if (data.pubDate) lines.push(`pubDate: ${data.pubDate}`);
  if (data.slug) lines.push(`slug: "${yamlEscape(data.slug)}"`);
  if (data.tags && data.tags.length) {
    lines.push('tags:');
    for (const t of data.tags) lines.push(`  - ${t}`);
  }
  lines.push('---', '');
  return lines.join('\n');
}

function convertOne(html, collection, slug) {
  const $ = load(html);

  const title = $('article h1').first().text().trim() || $('title').text().trim();
  const description = $('meta[name="description"]').attr('content') || '';
  const pubDate = $('article time').first().attr('datetime') || '';
  const tags = $('article header .bg-gray-100')
    .map((_, el) => $(el).text().trim())
    .get()
    .filter(Boolean);

  // Body lives in <div class="prose prose-lg max-w-none">
  const proseEl = $('article .prose').first();
  // Strip ids on headings (will be regenerated by markdown plugin)
  proseEl.find('[id]').removeAttr('id');
  // Drop scoped astro attributes
  proseEl.find('[data-astro-cid-evzfsq7n]').removeAttr('data-astro-cid-evzfsq7n');

  const bodyHtml = proseEl.html() || '';
  const bodyMd = turndown.turndown(bodyHtml).trim();

  // Preserve original case in URL by emitting an explicit slug. Astro
  // would otherwise lowercase the filename-derived slug.
  const fm = frontmatter({ title, description, pubDate, slug, tags });
  return fm + bodyMd + '\n';
}

function main() {
  for (const coll of COLLECTIONS) {
    const distColl = join(ROOT, 'dist', coll, 'posts');
    const outDir = join(ROOT, 'src', 'content', coll);
    ensureDir(outDir);
    for (const postDir of listDirs(distColl)) {
      const slug = postDir.split('/').pop();
      const indexPath = join(postDir, 'index.html');
      let html;
      try {
        html = readFileSync(indexPath, 'utf8');
      } catch {
        console.warn(`skip: ${indexPath} missing`);
        continue;
      }
      const md = convertOne(html, coll, slug);
      const outPath = join(outDir, `${slug}.md`);
      writeFileSync(outPath, md, 'utf8');
      console.log(`✓ ${coll}/${slug}.md`);
    }
  }
}

main();
